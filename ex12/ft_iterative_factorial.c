int	ft_iterative_factorial(int nb)
{
	int	i;
	int	f;

	f = 1;
	i = nb;
	if (nb < 0 || nb >= 13)
		return (0);
	if (nb == 0)
		return (1);
	while (i != 1)
	{
		f = f * i;
		i--;
	}
	return (f);
}
